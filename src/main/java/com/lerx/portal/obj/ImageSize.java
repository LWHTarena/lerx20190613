package com.lerx.portal.obj;

public class ImageSize {
	
	private int width;
	private int height;
	private int intact;
	
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getIntact() {
		return intact;
	}
	public void setIntact(int intact) {
		this.intact = intact;
	}

}
